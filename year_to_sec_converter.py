#Program to convert year to sec
no_of_year = 1
no_of_days_in_year = 365
no_of_hr_in_day = 24
no_of_mins_in_hr = 60
no_of_sec_in_mins = 60
no_of_sec_in_1_year = no_of_year*no_of_days_in_year*no_of_hr_in_day*no_of_mins_in_hr*no_of_sec_in_mins
print(f"No of sec in one year :- {no_of_sec_in_1_year} sec ")